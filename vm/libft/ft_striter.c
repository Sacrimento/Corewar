#include <string.h>

void	ft_striter(char *s, void (*f)(char*))
{
	int count;

	count = 0;
	if (s != NULL && f != NULL)
	{
		while (s[count] != '\0')
		{
			f(&s[count]);
			count++;
		}
	}
}
